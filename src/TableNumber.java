public class TableNumber {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int tableNumber = 4999;
		for (int i = 1; i <= 10; i++) 
		{
			int mulValue = tableNumber * i;
			System.out.println(tableNumber + " * " + i + " = " + mulValue);
			
		}

	}

}
